<h2>O FIRMIE</h2>
<div class="left">
<p>Właścicielem firmy jest mgr Urszula Szatna. Absolwentka studiów prawniczych na Uniwersytecie Wrocławskim. Wyróżniona stypendium naukowym za bardzo dobre wyniki w nauce. W 2006 r. nagrodzona dyplomem przez Burmistrza Miasta Nowa Ruda. Współpracuje z tygodnikiem lokalnym „Gazeta Noworudzka” i „Gazetą Kłodzką”.</p>
<p>Firma prawnicza działa już od kilku lat i zyskała zaufanie wielu klientów, w tym również z Hiszpanii, Włoch, Niemiec, Austrii, Czech i Wielkiej Brytanii.</p>
</div>
<div class="right">
<p>Oferuje szeroki zakres pomocy prawnej oraz wysoki standard świadczenia usług.</p>
<p>Wiedza merytoryczna, kompetencje, doświadczenie i wyjątkowe podejście do Klienta sprawiają, że skutecznie rozwiązuje wszelkie problemy prawne – zarówno osób fizycznych jak i podmiotów gospodarczych.</p>
</div>
